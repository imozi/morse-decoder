const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
  space: " ",
};

function decode(expr) {
  const exprMatch = expr.match(/\d{10}|\*{10}/g);

  return exprMatch.reduce((a, e) => {
    if (/\*{10}/.test(e)) return (a += MORSE_TABLE.space);

    const code = e
      .match(/\d{2}/g)
      .map((e) => (e === "11" ? "-" : e === "10" ? "." : ""))
      .join("");

    return (a += MORSE_TABLE[code]);
  }, "");
}

module.exports = {
  decode,
};
